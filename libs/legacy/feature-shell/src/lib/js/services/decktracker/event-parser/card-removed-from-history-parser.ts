import { GameState } from '@firestone/game-state';
import { GameEvent } from '@firestone/game-state';
import { DeckManipulationHelper } from './deck-manipulation-helper';
import { EventParser } from './event-parser';

export class CardRemovedFromHistoryParser implements EventParser {
	constructor(private readonly helper: DeckManipulationHelper) {}

	applies(gameEvent: GameEvent, state: GameState): boolean {
		return !!state;
	}

	async parse(currentState: GameState, gameEvent: GameEvent): Promise<GameState> {
		const [cardId, controllerId, localPlayer, entityId] = gameEvent.parse();

		const isPlayer = controllerId === localPlayer.PlayerId;
		const deck = isPlayer ? currentState.playerDeck : currentState.opponentDeck;
		const newPlayerDeck = deck.update({
			hand: deck.hand.filter((e) => e.entityId !== entityId),
			otherZone: deck.otherZone.filter((e) => e.entityId !== entityId),
			deck: deck.deck.filter((e) => e.entityId !== entityId),
			board: deck.board.filter((e) => e.entityId !== entityId),
		});
		return Object.assign(new GameState(), currentState, {
			[isPlayer ? 'playerDeck' : 'opponentDeck']: newPlayerDeck,
		});
	}

	event(): string {
		return GameEvent.REMOVE_FROM_HISTORY;
	}
}
